import Foundation

public class Calculator
{
    public func add( firstNum : Int, secondNum : Int ) -> Int
    {
        return ( firstNum + secondNum )
    }
    
    public func sub( firstNum : Int, secondNum : Int ) -> Int
    {
        return ( firstNum - secondNum )
    }
    
    public func mul( firstNum : Int, secondNum : Int ) -> Int
    {
        return ( firstNum * secondNum )
    }
    
    public func div( firstNum : Int, secondNum : Int ) -> Int
    {
        return ( firstNum / secondNum )
    }
}
